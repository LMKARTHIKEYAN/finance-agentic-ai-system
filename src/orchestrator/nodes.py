def kpi_node(
    state: FinanceGraphState,
) -> FinanceGraphState:
    """Run the existing KPIAgent."""

    node_name = "kpi"

    try:
        selected_flow = state.get("selected_flow", "unknown")

        requested_kpis = state.get("requested_kpis")

        if not requested_kpis:
            if selected_flow == "forecast":
                requested_kpis = [
                    "forecast_orders",
                    "forecast_revenue",
                    "forecast_aov",
                ]
            elif selected_flow == "kpi":
                requested_kpis = [
                    "total_orders",
                    "completed_orders",
                    "cancelled_orders",
                    "fulfillment_percentage",
                    "cancellation_percentage",
                    "actual_revenue",
                    "actual_aov",
                ]
            else:
                requested_kpis = DEFAULT_KPIS

        forecast_period = None
        scenario_period = None

        forecast_result = state.get("forecast_result")

        if forecast_result is not None:
            forecast_summary = getattr(
                forecast_result,
                "forecast_summary",
                None,
            )

            if forecast_summary:
                forecast_period = str(
                    forecast_summary[0]["forecast_period"]
                )

        scenario_result = state.get("scenario_result")

        if scenario_result is not None:
            adjusted_forecast = getattr(
                scenario_result,
                "adjusted_forecast",
                None,
            )

            if adjusted_forecast:
                scenario_period = str(
                    adjusted_forecast[0]["forecast_period"]
                )

        filters = _get_filters(state)

        result = KPIAgent().analyze(
            requested_kpis=requested_kpis,
            operations_result=state.get("operations_result"),
            budget_result=state.get("budget_result"),
            revenue_variance_result=state.get("variance_result"),
            forecast_result=forecast_result,
            scenario_result=scenario_result,
            finance_rules_result=state.get("finance_rules_result"),
            forecast_period=forecast_period,
            scenario_period=scenario_period,
            dimension=filters.get("dimension"),
            dimension_value=filters.get("dimension_value"),
        )

        return _record_success(
            state,
            node_name,
            requested_kpis=requested_kpis,
            kpi_result=result,
        )

    except Exception as error:
        return _record_failure(state, node_name, error)


