def complete_node(
    state: FinanceGraphState,
) -> FinanceGraphState:
    """
    Mark a successful route as completed.

    This is useful for routes such as budget, KPI, or forecast that may end
    without creating a complete management report.
    """

    if state.get("execution_status") == "failed":
        return state

    executed_nodes = list(state.get("executed_nodes", []))

    if "complete" not in executed_nodes:
        executed_nodes.append("complete")

    return {
        **state,
        "execution_status": "completed",
        "error_message": "",
        "failed_node": "",
        "executed_nodes": executed_nodes,
    }


def error_node(
    state: FinanceGraphState,
) -> FinanceGraphState:
    """
    Preserve a failed state before LangGraph execution ends.

    Individual nodes already record the original exception. This node ensures
    the graph ends with a consistent failed status.
    """

    errors = list(state.get("errors", []))
    error_message = state.get(
        "error_message",
        "Graph execution failed.",
    )

    if not error_message:
        error_message = "Graph execution failed."

    return {
        **state,
        "execution_status": "failed",
        "error_message": error_message,
        "errors": errors,
    }


def should_continue(state: FinanceGraphState) -> str:
    """
    Return the conditional graph route after any agent node.

    Returns:
        ``continue`` when execution can proceed.
        ``error`` when a previous node failed.
    """

    if state.get("execution_status") == "failed":
        return "error"

    return "continue"