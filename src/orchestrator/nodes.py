def validate_operations_node(
    state: FinanceGraphState,
) -> FinanceGraphState:
    """Validate raw operations data."""

    node_name = "validate_operations"

    try:
        operations_data = _require_dataframe(
            state,
            "operations_data",
            node_name,
        )

        validation_result = ValidationAgent.validate_operations_data(
            operations_data
        )

        if not validation_result.is_valid:
            details = "; ".join(validation_result.errors)
            raise ValueError(
                "Operations validation failed: "
                f"{details or 'Unknown validation error.'}"
            )

        return _record_success(
            state,
            node_name,
            operations_validation=validation_result,
            validation_result=validation_result,
        )

    except Exception as error:
        return _record_failure(state, node_name, error)


