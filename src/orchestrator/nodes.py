def _record_success(
    state: FinanceGraphState,
    node_name: str,
    **updates: Any,
) -> FinanceGraphState:
    """
    Return a successful state update while preserving existing state.

    Args:
        state:
            Current graph state.

        node_name:
            Name of the successfully completed node.

        **updates:
            New state values generated by the node.

    Returns:
        Updated FinanceGraphState.
    """

    executed_nodes = list(state.get("executed_nodes", []))

    if node_name not in executed_nodes:
        executed_nodes.append(node_name)

    return {
        **state,
        **updates,
        "execution_status": "running",
        "error_message": "",
        "failed_node": "",
        "errors": list(state.get("errors", [])),
        "executed_nodes": executed_nodes,
    }


def _record_failure(
    state: FinanceGraphState,
    node_name: str,
    error: Exception | str,
) -> FinanceGraphState:
    """
    Store a node failure in the shared state.

    Args:
        state:
            Current graph state.

        node_name:
            Name of the node that failed.

        error:
            Exception or error message.

    Returns:
        Failed FinanceGraphState.
    """

    error_message = str(error)
    errors = list(state.get("errors", []))
    errors.append(f"{node_name}: {error_message}")

    return {
        **state,
        "execution_status": "failed",
        "failed_node": node_name,
        "error_message": error_message,
        "errors": errors,
    }


def _require_dataframe(
    state: FinanceGraphState,
    field_name: str,
    node_name: str,
) -> pd.DataFrame:
    """
    Retrieve and validate a required DataFrame from state.

    Raises:
        ValueError:
            If the field is missing or the DataFrame is empty.

        TypeError:
            If the field is not a pandas DataFrame.
    """

    value = state.get(field_name)

    if value is None:
        raise ValueError(
            f"Required state field '{field_name}' is missing."
        )

    if not isinstance(value, pd.DataFrame):
        raise TypeError(
            f"State field '{field_name}' must be a pandas DataFrame "
            f"for node '{node_name}'."
        )

    if value.empty:
        raise ValueError(
            f"State field '{field_name}' contains an empty DataFrame."
        )

    return value


def _require_state_value(
    state: FinanceGraphState,
    field_name: str,
) -> Any:
    """
    Retrieve a required result from state.

    Raises:
        ValueError:
            If the required value is missing.
    """

    value = state.get(field_name)

    if value is None:
        raise ValueError(
            f"Required state field '{field_name}' is missing."
        )

    return value


def _get_filters(state: FinanceGraphState) -> dict[str, Any]:
    """Return graph filters or an empty dictionary."""

    filters = state.get("filters", {})

    if filters is None:
        return {}

    if not isinstance(filters, dict):
        raise TypeError("State field 'filters' must be a dictionary.")

    return filters


def _get_group_by(
    state: FinanceGraphState,
    default: str = "month",
) -> str:
    """Return a valid single group-by value from state."""

    group_by = state.get("group_by")

    if group_by is None:
        return default

    if not isinstance(group_by, str):
        raise TypeError(
            "State field 'group_by' must be a string for the current agents."
        )

    return group_by


